const expenseForm = document.getElementById('expenseForm');
const expenseList = document.getElementById('expenseList');

// Fetch expenses when the page loads
fetchExpenses();

expenseForm.addEventListener('submit', (event) => {
    event.preventDefault();

    const amount = document.getElementById('amount').value;
    const description = document.getElementById('description').value;
    const category = document.getElementById('category').value;

    const token = localStorage.getItem('token');

    fetch('/expenses', {
        method: 'POST',
        headers: {
            'Content-Type': 'application/json',
            'Authorization': `Bearer ${token}`,
        },
        body: JSON.stringify({ amount, description, category }),
    })
    .then(response => {
        if (!response.ok) {
            return response.text().then((message) => { throw new Error(message); });
        }
        return response.text();
    })
    .then(data => {
        console.log("Expense added", data);
        fetchExpenses();
        // Clear input fields
        document.getElementById('amount').value = '';
        document.getElementById('description').value = '';
        document.getElementById('category').value = ''; 
    })
    .catch(error => {
        console.log("Error adding expense", error.message);
    });
});

// Function to fetch expenses
function fetchExpenses() {
    const token = localStorage.getItem('token');

    fetch('/expenses', {
        method: 'GET',
        headers: {
            'Authorization': `Bearer ${token}`,
        },
    })
    .then(response => {
        if (!response.ok) {
            throw new Error('Failed to fetch expenses');
        }
        return response.json();
    })
    .then(expenses => {
        expenseList.innerHTML = '';
        expenses.forEach(expense => {
            const li = document.createElement('li');
            li.textContent = `${expense.description}: ${expense.amount} (${expense.category})`;
            const deleteButton = document.createElement('button');
            deleteButton.textContent = 'Delete';
            deleteButton.onclick = () => deleteExpense(expense.expense_id);
            li.appendChild(deleteButton);
            expenseList.appendChild(li);
        });
    })
    .catch(error => {
        console.log("Error fetching expenses", error.message);
    });
}

// Function to delete expense
function deleteExpense(expenseId) {
    const token = localStorage.getItem('token');

    fetch(`/expenses/${expenseId}`, {
        method: 'DELETE',
        headers: {
            'Authorization': `Bearer ${token}`,
        },
    })
    .then(response => {
        if (!response.ok) {
            throw new Error('Failed to delete expense');
        }
        fetchExpenses(); // Refresh expense list
    })
    .catch(error => {
        console.log("Error deleting expense", error.message);
    });
}

// Buy premium membership function
function buyPremium() {
    // Create an order by sending a request to the server
    fetch('/create-order', {
        method: 'POST',
    })
    .then(response => response.json())
    .then(order => {
        // Initialize Razorpay checkout form
        const options = {
            key: 'rzp_test_N8ErFdSrLpInkD', // Razorpay Key ID
            amount: order.amount, // Order amount
            currency: order.currency,
            name: 'Expense App',
            description: 'Buy Premium Membership',
            order_id: order.id, // This is the order ID generated by Razorpay
            handler: function (response) {
                // Handle successful payment
                alert('Payment successful!');
                updateUserToPremium();
            },
            theme: {
                color: '#3399cc'
            }
        };

        const rzp1 = new Razorpay(options);
        rzp1.open();
    })
    .catch(error => {
        console.error('Error creating order:', error);
    });
}

// Update the user to premium after payment
function updateUserToPremium() {
    const token = localStorage.getItem('token'); // Get the JWT token from local storage

    fetch('/update-to-premium', {
        method: 'POST',
        headers: {
            'Content-Type': 'application/json',
            'Authorization': `Bearer ${token}`, // Include the token in the Authorization header
        },
    })
    .then(response => {
        if (!response.ok) {
            throw new Error('Error updating user status to premium');
        }
        // Change button text to show user is premium
        document.getElementById('buyPremium').textContent = 'You are a premium user';
        document.getElementById('buyPremium').disabled = true;

        // Load leaderboard data after the user is updated to premium
        loadLeaderboard();
    })
    .catch(error => {
        console.error('Error updating user:', error.message); // Log the error message for debugging
    });
}

// Load leaderboard for premium users
async function loadLeaderboard() {
    const token = localStorage.getItem('token');
    const response = await fetch('/leaderboard', {
        method: 'GET',
        headers: {
            'Authorization': `Bearer ${token}`,
        },
    });

    const leaderboardMessage = document.getElementById('leaderboardMessage');
    const leaderboard = document.getElementById('leaderboard');

    if (response.ok) {
        const expenses = await response.json();
        leaderboard.innerHTML = ''; // Clear leaderboard

        expenses.forEach(expense => {
            const listItem = document.createElement('li');
            listItem.textContent = `${expense.description} - ${expense.amount} (${expense.category}) by User ID ${expense.user_id}`;
            leaderboard.appendChild(listItem);
        });
    } else {
        leaderboardMessage.textContent = 'You must be a premium user to see the leaderboard.';
    }
}

